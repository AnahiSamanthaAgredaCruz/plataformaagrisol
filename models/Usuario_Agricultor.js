const mongoose = require('mongoose');

const Usuario_AgricultorSchema = mongoose.Schema({
  nombre: {
    type: String,
    required: true,
    trim: true
  },
  apellido: {
    type: String,
    required: true,
    trim: true
  },
  carnet_identidad: {
    type: String,
    required: true,
    trim: true
  },
  direccion: {
    type: String,
    required: true,
    trim: true
  },
  telefono: {
    type: String,
    required: true,
    trim: true
  },
  correo_electronico: {
    type: String,
    required: true,
    trim: true,
    unique: true
  },
  password: {
    type: String,
    required: true,
    trim: true
  },
  creado: {
    type: Date,
    default: Date.now()
  }
});

module.exports = mongoose.model('Usuario_Agricultor', Usuario_AgricultorSchema);
